% Generate Quadruple Range quadrature
function quadInfo = QuadRangeQuad()

% Number of polar angles
nPolar = 12;

% Number of azimuthal angles per polar angle
nAzi = zeros(1, nPolar);
for i = 1 : nPolar
    nAzi(i) = i;
end

% Number of angles
numAng = sum(nAzi);

% Resize storage
quadInfo = zeros(numAng, 5);

% Retrieve cosTheta and weight from table for polar angle
[cosTheta, polarWgt] = getPolar(nPolar);

% Go through each polar level
numDir = 1;
for i = 1 : nPolar
    % Retrieve cosPhi and weight from table for azimuthal angle
    [cosPhi, aziWgt] = getAzi(nAzi(i));
    for j = 1 : nAzi(i)
        quadInfo(numDir, 1) = numDir;
        quadInfo(numDir, 2) = sqrt(1 - cosTheta(i) ^ 2) * cosPhi(j);
        quadInfo(numDir, 3) = sqrt(1 - cosTheta(i) ^ 2) * sqrt(1 - cosPhi(j) * cosPhi(j));
        quadInfo(numDir, 4) = cosTheta(i);
        quadInfo(numDir, 5) = polarWgt(i) * aziWgt(j);
        numDir = numDir + 1;
    end
end

end

